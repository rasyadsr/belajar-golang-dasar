package main

import "fmt"

func main() {

	var result = 10 + 10
	fmt.Println(result)

	fmt.Println(10 / 2)
	fmt.Println(19 - 10)
	fmt.Println(2 * 5)
	fmt.Println(5 % 2)

	var i int = 10
	i += 10 // i = i + 10
	fmt.Println(i)
	i++ // i = i + 1
	fmt.Println(i)

	var negative = -10
	fmt.Println(negative)


}